public class ListNode {


    /**
     * 链表的值
     */
    public int val;

    /**
     * 链表的next节点
     */
    public ListNode next;

    /**
     * 构造方法
     * @param x value
     */
    public ListNode(int x) {
        val = x;
    }

}
